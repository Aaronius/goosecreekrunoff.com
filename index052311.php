<font face=arial size=3><b>

KIDS RACE

</b></font><p>

A 1-mile kid's race around town will be held at approximately 8:30am for children <b>under</b> 12 years of age. Check in by the Oakley City Office at 8:00am. A $3.00 fee and a signed waiver are required.

