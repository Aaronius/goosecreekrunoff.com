<font face=arial size=3><b>

10K RACE

</b></font><p>

The 10K will start above the Oakley Reservoir, as it did last year.  The distance is a GPS measured 10K.  The course follows a scenic, winding road from above the dam, across the Goose Creek drainage and into town, ending at the Oakley City Park.
